    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Koneksi c = new Koneksi();
        try {
            User u = new User();
            this.stat = c.getConn().prepareStatement("INSERT INTO users (username, password, email, role) VALUES (?, ?, ?, ?)");
            stat.setString(1, u.username);
            stat.setString(2, u.password);
            stat.setString(3, u.email);
            stat.setString(4, u.role); // Pastikan ini adalah string
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        Koneksi c = new Koneksi();
        try {
            User u = new User();
            this.stat = c.getConn().prepareStatement("update users set username=?,"
                    + "password=?,email=?,role=? where id=?");
            stat.setString(1, u.username);
            stat.setString(2, u.password);
            stat.setString(3, u.email);
            stat.setString(4, u.role);
            stat.setString(5, id.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

