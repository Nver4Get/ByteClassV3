    class User extends UserForm {
        int id;
        String username, password, email, role;

        public User() {
            username = usernameTxt.getText();
            email = emailTxt.getText();
            password = passwordTxt.getText();
            String selectedRole = roleCbx.getSelectedItem().toString();

            // Pastikan mencocokkan dengan casing yang benar
            if ("user".equalsIgnoreCase(selectedRole)) {
                role = "user"; // Misalkan 0 untuk user
            } else if ("admin".equalsIgnoreCase(selectedRole)) {
                role = "admin"; // Misalkan 1 untuk admin
            }
        }
    }

    public void refreshTable() {
        Koneksi c = new Koneksi();
        model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Username");
        model.addColumn("Email");
        model.addColumn("Password");
        model.addColumn("Role");
        tblUser.setModel(model);
        try {
            this.stat = c.getConn().prepareStatement("select * from users");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {rs.getString("id"),
                    rs.getString("username"),
                    rs.getString("email"),
                    rs.getString("password"),
                    rs.getString("role")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        id.setText("");
        usernameTxt.setText("");
        emailTxt.setText("");
        passwordTxt.setText("");
    }

